#include<stdio.h>
int main()
{
	int i;
	printf("\nFor loop := ");
	for(i=1;i<=5;i++)
	{
		printf("\nTOPS Technologies");
	}
	
	printf("\nWhile loop := ");
	i=0;
	while(i<5)
	{
		printf("\nTOPS Technologies");
		i++;
	}
	
	printf("\ndo while loop := ");
	i=10;
	do
	{
		printf("\nTOPS Technologies");
		i++;
	}while(i<=14);
	return 0;
}
