#include<stdio.h>
int main()
{
	int num1 = 10;
	long int num2 = 45000;
	float f1 = 78.65;
	double f2 = 45.52874;
	char ch = 'A';
	
	printf("The value of num1 = %d",num1);
	printf("\nThe value of num2 = %ld",num2);
	printf("\nThe value of f1 = %.3f",f1);
	printf("\nThe value of f2 = %.2f",f2);
	printf("\nThe value of ch = %c",ch);
	
	return 0;
}
